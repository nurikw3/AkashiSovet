def build_free_form_system(tpl: ApplicationTemplate) -> str:
    n = tpl.total_blocks_count
    lines = [
        "Ты — проактивный ассистент ПК «AKASHI Data Center PLC».",
        f"Твоя цель — собрать данные для {n} блоков служебной записки.",
        "",
        "═══ АБСОЛЮТНЫЙ ЗАПРЕТ (важнее всех остальных правил) ═══",
        "Никогда не добавляй факты, которых нет в словах пользователя:",
        "— даты, периоды, годы",
        "— названия документов, приказов, регламентов, номера актов",
        "— имена, должности, названия отделов",
        "— конкретные цифры, суммы, проценты",
        "— детали исследований или аудитов, которые пользователь не упомянул",
        "Если пользователь сказал «был аудит» — пиши «по результатам аудита», без дат и деталей.",
        "",
        "═══ ПРИМЕР ═══",
        "Пользователь: «нужно обновить охлаждение, был аудит»",
        "✓ «По результатам проведённого аудита выявлена необходимость модернизации системы охлаждения.»",
        "✗ «По результатам аудита от марта 2024 г. ...» — дату не называл, добавлять запрещено.",
        "",
        "═══ БЛОКИ ═══",
    ]
    for idx, b in enumerate(tpl.blocks, start=1):
        lines.append(f"{idx}. {b.title} — {b.question}")
    lines += [
        "",
        "═══ ПРАВИЛА ═══",
        "— Обязательно вызывай одну из функций: ask_user или submit_memo.",
        "— Если не хватает данных для части блоков — вызови ask_user с одним коротким вопросом.",
        "— Не задавай общих вопросов о политиках компании.",
        "— При submit_memo стилизуй текст официально, но включай ТОЛЬКО то, что сказал пользователь.",
        "— Не подставляй вымышленные имена или XXX вместо названий из сообщений пользователя.",
        "— В тексте блоков используй обычный дефис (-), чтобы документ корректно отображался в PDF.",
        f"— {STRUCTURED_LIST_RULE}",
        f"— {OUTPUT_RUSSIAN_CYRILLIC}",
        "— Это правило действует и для текста в ask_user (если перечисляешь варианты или шаги), "
        "и для значений полей в submit_memo.",
    ]
    return "\n".join(lines)


def build_free_form_tools(submit_memo_model: type[BaseModel]) -> list[dict[str, Any]]:
    return [
        {
            "type": "function",
            "function": {
                "name": "ask_user",
                "description": "Задать уточняющий вопрос пользователю.",
                "parameters": AskUser.model_json_schema(),
            },
        },
        {
            "type": "function",
            "function": {
                "name": "submit_memo",
                "description": "Сформировать готовые блоки служебной записки по текущему шаблону.",
                "parameters": submit_memo_model.model_json_schema(),
            },
        },
    ]
