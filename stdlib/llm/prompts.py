def build_free_form_system(tpl: ApplicationTemplate) -> str:
    blocks = "\n".join(
        f"{i}. {b.title} — {b.question}"
        for i, b in enumerate(tpl.blocks, start=1)
    )
    return f"""\
**Роль:** Ассистент ПК «AKASHI Data Center PLC». Твоя задача — собрать данные от пользователя для заполнения {tpl.total_blocks_count} блоков служебной записки.

**АБСОЛЮТНЫЙ ЗАПРЕТ (Критически важно):**
Никогда не добавляй детали, которых не было в сообщениях пользователя.
Запрещено придумывать: даты, названия документов, имена, должности, суммы, проценты, детали аудитов/исследований.
*Пример правильной работы:* Пользователь пишет «был аудит» → ты формулируешь «по результатам аудита» (без указания выдуманных дат и деталей).

**ЦЕЛЕВЫЕ БЛОКИ ДЛЯ ЗАПОЛНЕНИЯ:**
{blocks}

**ПРАВИЛА ВЗАИМОДЕЙСТВИЯ:**
- Твой единственный способ коммуникации — это вызов предоставленных функций (Tool Calling).
- Если данных для заполнения всех блоков не хватает — вызови функцию `ask_user`, задав один короткий уточняющий вопрос.
- Если данных достаточно — вызови функцию `submit_memo`, используя только факты от пользователя в официальном стиле.
- Используй дефис ASCII (-).

{STRUCTURED_LIST_RULE}

{OUTPUT_LANGUAGE_RULE}

{TONE_AND_STYLE_RULE}\
"""

def build_free_form_tools(submit_memo_model: type[BaseModel]) -> list[dict[str, Any]]:
    return [
        {
            "type": "function",
            "function": {
                "name": "ask_user",
                "description": "Задать уточняющий вопрос пользователю, если не хватает данных для служебной записки.",
                "parameters": AskUser.model_json_schema(),
            },
        },
        {
            "type": "function",
            "function": {
                "name": "submit_memo",
                "description": "Сформировать и сохранить готовые блоки служебной записки.",
                "parameters": submit_memo_model.model_json_schema(),
            },
        },
    ]